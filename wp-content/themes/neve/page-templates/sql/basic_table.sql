CREATE TABLE `account` (
  `ACT_ID` int(11) NOT NULL,
  `ACT_FIRST_NAME` varchar(50) NOT NULL,
  `ACT_PLAN` varchar(10) NOT NULL,
  `ACT_ADDRESS_LINE1` varchar(250) NOT NULL,
  `ACT_EMAIL` varchar(70) NOT NULL,
  `ACT_PHONE` varchar(15) NOT NULL,
  `ACT_CREATION_DATE` datetime NOT NULL,
  `ACT_NOTIF_EMAIL` varchar(1) NOT NULL,
  `ACT_NOTIF_SMS` varchar(1) DEFAULT NULL,
  `ACT_LAST_NAME` varchar(50) NOT NULL,
  `ACT_LAST_UPDATE` datetime DEFAULT NULL,
  `ACT_COUNTRY` varchar(30) NOT NULL,
  `ACT_AUTO_PULSE_CHECK` varchar(1) NOT NULL,
  `ACT_PULSE_CHECK_FREQ` int(11) NOT NULL,
  `ACT_ADDRESS_LINE2` varchar(250) DEFAULT NULL,
  `ACT_CITY` varchar(80) NOT NULL,
  `ACT_STATE_PROVINCE` varchar(50) DEFAULT NULL,
  `ACT_POSTAL_CODE` varchar(10) NOT NULL,
  `ACT_BIRTH_YEAR` int(11) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
